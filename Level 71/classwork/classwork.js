// Math.PI - აბრუნებს კონსტანტს
// Math.E - აბრუნებს კონსტანტს
// Math.floor - დაბლა ამრგვალებს ფლოატს ინტეჯერამდე
// Math.trunc - ფლოატს შორებს რიცხვიდან
// Math.round - ფლოატის ინტეჯერდან დამრგვალება
// Math.ceil - ფოათის დამრგვალება
// Math.pow - ხარისხში აყვანა
// Math.random - რანდომ რიცხვი 0 დან x
// Math.abs - მოდული
// Math.sqrt - ფესვი

let x = 5

console.log(Math.PI(x));
console.log(Math.E(x));
console.log(Math.floor(x));
console.log(Math.trunc(x));
console.log(Math.round(x));
console.log(Math.ceil(x));
console.log(Math.pow(x, 5));
console.log(Math.random(x));
console.log(Math.abs(x));
console.log(Math.sqrt(x));


